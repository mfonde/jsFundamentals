/**
 * Comparison Operators
 */

 // Equal
'3' == 3;  // true

// Strictly Equal
'3' === 3; //  false
 3  === 3; //  true

 // Not Equal
'3' != 3; // false

// Strict Not Equal
'3' !== 3; //true

// Greater Than
3 > 2; // true

// Less Than
2 < 3;  // true

//Greater Than or Equal To
3 >= 2; //NOT THE SAME AS =>

// Less Than or Equal To
2 <= 3; 

// And

3 > 2 && 2 < 3;

// Or

3 > 2 || 2 > 3;
